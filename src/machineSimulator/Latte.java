package machineSimulator;

public class Latte {
    private Integer water=350;
    private Integer milk=75;
    private Integer beans=20;
    private Integer price=7;

    public Latte() {
    }

    public Integer getWater() {
        return water;
    }

    public Integer getMilk() {
        return milk;
    }

    public Integer getBeans() {
        return beans;
    }

    public Integer getPrice() {
        return price;
    }
}
